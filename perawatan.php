      <p class="lead">
        Perawatan tanaman cabai meliputi beberapa aspek penting yang harus diperhatikan:
      </p>
      <ul>
        <li>
          <strong>Kelembaban:</strong>
          <ul>
            <li><strong>Penyiraman:</strong> Tanaman cabai membutuhkan penyiraman yang cukup, terutama selama musim kemarau. Idealnya, tanah harus tetap lembab tetapi tidak tergenang air. Siram tanaman secara teratur, terutama saat pagi atau sore hari untuk mengurangi penguapan.</li>
            <li><strong>Drainase:</strong> Pastikan tanah memiliki drainase yang baik untuk mencegah genangan air yang bisa menyebabkan akar busuk.</li>
          </ul>
        </li>
        <li>
          <strong>Suhu:</strong>
          <ul>
            <li><strong>Suhu Ideal:</strong> Tanaman cabai tumbuh optimal pada suhu antara 21째C hingga 30째C. Suhu di bawah 15째C atau di atas 35째C bisa menghambat pertumbuhan dan produksi buah.</li>
            <li><strong>Perlindungan dari Cuaca Ekstrem:</strong> Jika suhu terlalu tinggi, berikan peneduh atau penyiraman ekstra untuk menjaga kelembaban. Jika suhu terlalu rendah, lindungi tanaman dengan plastik mulsa atau penutup tanaman.</li>
          </ul>
        </li>
        <li>
          <strong>Kesuburan Tanah:</strong>
          <ul>
            <li><strong>Pupuk Organik:</strong> Gunakan pupuk organik seperti kompos atau pupuk kandang untuk memperbaiki struktur tanah dan meningkatkan kandungan hara.</li>
            <li><strong>Pupuk Anorganik:</strong> Berikan pupuk NPK dengan kandungan nitrogen, fosfor, dan kalium yang seimbang. Pemberian pupuk dilakukan secara berkala, biasanya setiap 2-3 minggu sekali.</li>
            <li><strong>pH Tanah:</strong> Pastikan pH tanah berkisar antara 6.0 hingga 6.8. Jika pH tanah terlalu rendah (asam), tambahkan kapur dolomit untuk menetralkannya. Jika terlalu tinggi (basa), tambahkan belerang atau bahan organik untuk menurunkannya.</li>
          </ul>
        </li>
        <li>
          <strong>Perawatan Tambahan:</strong>
          <ul>
            <li><strong>Penyiangan:</strong> Lakukan penyiangan secara rutin untuk menghilangkan gulma yang dapat bersaing dengan tanaman cabai dalam mendapatkan nutrisi.</li>
            <li><strong>Pemangkasan:</strong> Pangkas daun dan cabang yang mati atau sakit untuk mencegah penyebaran penyakit dan memperbaiki sirkulasi udara.</li>
            <li><strong>Pengendalian Hama dan Penyakit:</strong> Monitor tanaman secara rutin untuk mengidentifikasi tanda-tanda serangan hama atau penyakit. Gunakan pestisida alami atau kimia jika diperlukan, tetapi pastikan untuk mengikuti petunjuk penggunaan.</li>
          </ul>
        </li>
        <li>
          <strong>Penggunaan Teknologi:</strong>
          <ul>
            <li><strong>Sensor Kelembaban Tanah:</strong> Gunakan sensor kelembaban tanah untuk memantau kadar air secara real-time. Sensor ini dapat membantu menentukan waktu yang tepat untuk penyiraman.</li>
            <li><strong>Pengukur Suhu dan pH:</strong> Alat ini membantu memantau kondisi suhu dan pH tanah, sehingga Anda dapat melakukan tindakan pencegahan lebih awal jika kondisi tidak sesuai.</li>
          </ul>
        </li>
      </ul>
